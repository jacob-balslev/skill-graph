#!/usr/bin/env node
/**
 * Guard: NO hand-written current-corpus skill count may live in an active doc.
 *
 * WHY THIS EXISTS
 * ---------------
 * Skill counts (canonical / marketplace / active / exported) change every time
 * a skill is added, removed, or migrated. For a long time the "current count"
 * was hand-stamped into `SKILL_GRAPH.md § Current State` AND copied into README,
 * ADRs, status docs, and onboarding pages. Every corpus change then forced a
 * multi-file sweep to re-stamp a number — pure token/maintenance waste, and the
 * numbers drifted out of sync anyway (137 → 141 → 145 in one phase alone).
 *
 * The fix is single-source-of-truth by GENERATION, not by discipline:
 *   - The ONLY place a current count is written is the generated status doc
 *     `docs/status.generated.md` (owned by `scripts/build-status-doc.js`) and
 *     the live shell command (`find ~/Development/skills/skills -name SKILL.md | wc -l`).
 *   - Every other doc LINKS to those; it never restates the number.
 *
 * This guard enforces (b): it fails CI if a 2–4 digit current-corpus count
   * assertion appears in any active doc. Historical records (CHANGELOG, ADRs,
   * plans, research, audits) are allowlisted — their counts are dated
 * as-of snapshots and must NOT be updated (updating them is the very waste this
 * guard prevents).
 *
 * Usage:
 *   node scripts/check-inline-skill-counts.js          # scan tracked *.md, exit 1 on violation
 *   node scripts/check-inline-skill-counts.js --json    # machine-readable findings
 */

'use strict';

const path = require('path');
const { execFileSync } = require('child_process');
const fs = require('fs');

const REPO_ROOT = path.resolve(__dirname, '..');

// Active-doc allowlist: a path is SKIPPED (counts allowed) when it is a
// generated artifact or a historical/dated record. Historical counts are
// as-of snapshots — never re-stamped — so they are not drift.
function isAllowlisted(rel) {
  const parts = rel.split('/');
  const base = path.basename(rel);
  if (base === 'status.generated.md') return true;        // the generated count home
  if (base === 'CHANGELOG.md') return true;               // release history
  if (parts.some(p => p === '_drafts')) return true;
  if (parts[0] === 'audits') return true;                 // dated audit reports
  if (parts[0] === 'benchmarks') return true;             // experiment snapshots
  if (parts[0] === 'marketplace') return true;            // generated export surface
  if (parts[0] === 'docs' && ['research', 'migrations', 'adr', 'plans'].includes(parts[1])) return true;
  return false;
}

// A current-corpus count assertion: a 2–4 digit number adjacent to a
// corpus-count noun, tolerant of markdown emphasis (** ` _) between them.
// Tuned to assertions of library size, NOT rule ranges ("5–25 skills per
// subject"), query counts, or version integers.
const SEP = '[*`_\\s]{0,4}';
const PATTERNS = [
  new RegExp(`\\b\\d{2,4}${SEP}SKILL\\.md\\b`, 'i'),
  new RegExp(`\\b\\d{2,4}${SEP}(?:canonical|active|exported|published|total)${SEP}skills?\\b`, 'i'),
  new RegExp(`(?:canonical|marketplace|exported|active)${SEP}skill${SEP}count[:|*\`\\s]{1,6}\\*{0,2}\\d{2,4}`, 'i'),
];

// Lines that legitimately mention a count noun without asserting the corpus
// size (the per-subject balance RULE, not a live count).
const FALSE_POSITIVE = [
  /\d{1,2}\s*[–-]\s*\d{1,2}\s+skills?\s+(?:per|each)/i,   // "5–25 skills per subject"
  /Miller'?s\s+\d/i,
];

function trackedMarkdown() {
  const out = execFileSync('git', ['ls-files', '*.md'], { cwd: REPO_ROOT, encoding: 'utf8' });
  return out.split('\n').map(s => s.trim()).filter(Boolean);
}

function main() {
  const json = process.argv.includes('--json');
  const findings = [];

  for (const rel of trackedMarkdown()) {
    if (isAllowlisted(rel)) continue;
    const abs = path.join(REPO_ROOT, rel);
    let text;
    try { text = fs.readFileSync(abs, 'utf8'); } catch { continue; }
    const lines = text.split(/\r?\n/);
    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      if (FALSE_POSITIVE.some(re => re.test(line))) continue;
      const hit = PATTERNS.find(re => re.test(line));
      if (hit) {
        findings.push({ file: rel, line: i + 1, text: line.trim().slice(0, 160) });
      }
    }
  }

  if (json) {
    process.stdout.write(JSON.stringify({ findings }, null, 2) + '\n');
    process.exit(findings.length ? 1 : 0);
  }

  if (findings.length === 0) {
    process.stdout.write('OK   no inline skill counts in active docs\n');
    process.exit(0);
  }

  process.stderr.write(`FAIL inline-skill-counts: ${findings.length} hand-written corpus count(s) in active docs.\n`);
  process.stderr.write('Counts live ONLY in docs/status.generated.md (generated by build-status-doc.js) and the live\n');
  process.stderr.write('`find ~/Development/skills/skills -name SKILL.md | wc -l` command. Link to those; never restate the number.\n');
  process.stderr.write('See AGENTS.md § Doc Ownership Map (current corpus state). Allowlisted: status.generated.md, CHANGELOG,\n');
  process.stderr.write('docs/{research,migrations,adr,plans}/, audits/, benchmarks/, marketplace/.\n\n');
  for (const f of findings) {
    process.stderr.write(`  ${f.file}:${f.line}  ${f.text}\n`);
  }
  process.exit(1);
}

if (require.main === module) main();

module.exports = { isAllowlisted, PATTERNS };
