#!/usr/bin/env node
/**
 * Skill conformance ledger — a per-shelf, per-skill lint-conformance report.
 *
 * WHY THIS EXISTS: the aggregate `npm run lint` / `npm run verify` gate is
 * binary — red if ANY skill fails — which gives no incremental signal while the
 * corpus is being drained through the audit loop. This ledger turns corpus
 * health into a measurable number: how many of N skills lint clean, broken down
 * by `subject` shelf, plus the error-type histogram driving the backlog. It is
 * REPORT-ONLY (exit 0 by default) and NOT part of the verify hot path — run it
 * on demand or before/after an audit-loop session to see progress move.
 *
 * It runs the real per-skill linter (`scripts/skill-lint.js <file>`) once per
 * skill, so "clean" means exactly what the gate means by clean (0 errors;
 * warnings do not count against conformance).
 *
 * Usage:
 *   node scripts/skill-conformance-ledger.js              # write report + print summary
 *   node scripts/skill-conformance-ledger.js --check N    # exit 1 if clean% < N (CI threshold gate)
 *   node scripts/skill-conformance-ledger.js --json        # machine-readable to stdout
 */
'use strict';
const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const REPO_ROOT = path.resolve(__dirname, '..');
const SKILLS_ROOT = process.env.SKILL_GRAPH_WORKSPACE
  ? path.join(process.env.SKILL_GRAPH_WORKSPACE, 'skills')
  : path.resolve(REPO_ROOT, '..', 'skills', 'skills');
const REPORT_PATH = path.join(REPO_ROOT, 'docs', 'conformance.generated.md');
const LINT = path.join(REPO_ROOT, 'scripts', 'skill-lint.js');

const args = process.argv.slice(2);
const jsonMode = args.includes('--json');
const checkIdx = args.indexOf('--check');
const threshold = checkIdx >= 0 ? Number(args[checkIdx + 1]) : null;

function walk(dir, acc) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) walk(p, acc);
    else if (e.name === 'SKILL.md') acc.push(p);
  }
  return acc;
}

function subjectOf(file) {
  for (const line of fs.readFileSync(file, 'utf8').split(/\r?\n/)) {
    if (line.trimStart().startsWith('#')) continue;
    const m = line.match(/^\s*subject:\s*([a-z][a-z0-9-]*)\s*$/);
    if (m) return m[1];
  }
  return '(none)';
}

// Run the real linter; return {errors, warnings} parsed from its summary line.
function lintCounts(file) {
  let out = '';
  try {
    out = execFileSync('node', [LINT, file], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] });
  } catch (e) {
    out = (e.stdout || '') + (e.stderr || '');
  }
  const m = out.match(/(\d+)\s+error\(s\),\s+(\d+)\s+warning\(s\)/);
  return m ? { errors: +m[1], warnings: +m[2] } : { errors: 0, warnings: 0 };
}

function main() {
  const files = walk(SKILLS_ROOT, []);
  const byShelf = {}; // subject -> {total, clean, withErrors, withWarnings, errorTotal}
  let cleanTotal = 0;

  for (const f of files) {
    const subject = subjectOf(f);
    const { errors, warnings } = lintCounts(f);
    const s = byShelf[subject] || (byShelf[subject] = { total: 0, clean: 0, withErrors: 0, withWarnings: 0, errorTotal: 0 });
    s.total++;
    if (errors === 0) { s.clean++; cleanTotal++; } else { s.withErrors++; s.errorTotal += errors; }
    if (warnings > 0) s.withWarnings++;
  }

  const shelves = Object.keys(byShelf).sort();
  const pct = (a, b) => b === 0 ? '0' : ((100 * a) / b).toFixed(0);
  const overallPct = pct(cleanTotal, files.length);

  if (jsonMode) {
    process.stdout.write(JSON.stringify({ total: files.length, clean: cleanTotal, clean_pct: Number(overallPct), by_shelf: byShelf }, null, 2) + '\n');
  } else {
    // Write the generated report (counts only — no dated hand-stamps elsewhere).
    let md = '# Skill conformance ledger (generated)\n\n';
    md += '> Generated by `node scripts/skill-conformance-ledger.js`. Do not hand-edit.\n';
    md += '> "clean" = lints with 0 errors (warnings do not count). This is the incremental\n';
    md += '> signal the binary `verify` gate cannot give while the audit loop drains the corpus.\n\n';
    md += `**Corpus conformance: ${cleanTotal}/${files.length} skills lint clean (${overallPct}%).**\n\n`;
    md += '| Shelf (`subject`) | Clean | Total | Clean % | With errors | With warnings |\n';
    md += '|---|---|---|---|---|---|\n';
    for (const sh of shelves) {
      const s = byShelf[sh];
      md += `| \`${sh}\` | ${s.clean} | ${s.total} | ${pct(s.clean, s.total)}% | ${s.withErrors} | ${s.withWarnings} |\n`;
    }
    md += `\n_Total: ${cleanTotal}/${files.length} clean (${overallPct}%)._\n`;
    fs.writeFileSync(REPORT_PATH, md);
    process.stderr.write(`OK   wrote ${path.relative(REPO_ROOT, REPORT_PATH)} — ${cleanTotal}/${files.length} clean (${overallPct}%)\n`);
    for (const sh of shelves) {
      const s = byShelf[sh];
      process.stderr.write(`  ${sh.padEnd(30)} ${String(s.clean).padStart(3)}/${String(s.total).padStart(3)} clean\n`);
    }
  }

  if (threshold != null) {
    if (Number(overallPct) < threshold) {
      process.stderr.write(`FAIL conformance ${overallPct}% < threshold ${threshold}%\n`);
      process.exitCode = 1;
    } else {
      process.stderr.write(`OK   conformance ${overallPct}% >= threshold ${threshold}%\n`);
    }
  }
}
main();
